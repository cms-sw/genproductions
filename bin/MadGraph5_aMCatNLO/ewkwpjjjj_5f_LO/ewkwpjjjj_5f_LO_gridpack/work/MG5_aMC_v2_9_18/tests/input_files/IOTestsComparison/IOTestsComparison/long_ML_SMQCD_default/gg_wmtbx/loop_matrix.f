      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=5)
C     
C     ARGUMENTS 
C     
      REAL*8 P(0:3,NEXTERNAL)
      REAL*8 ANS(3)
      INTEGER HEL, USERHEL
      COMMON/USERCHOICE/USERHEL
