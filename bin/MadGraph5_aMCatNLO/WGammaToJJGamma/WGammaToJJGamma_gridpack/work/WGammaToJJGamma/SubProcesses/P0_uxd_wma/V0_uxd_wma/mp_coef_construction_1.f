      SUBROUTINE MP_COEF_CONSTRUCTION_1(P,NHEL,H,IC)
C     
      USE POLYNOMIAL_CONSTANTS
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=4)
      INTEGER    NCOMB
      PARAMETER (NCOMB=24)

      INTEGER NBORNAMPS
      PARAMETER (NBORNAMPS=3)
      INTEGER    NLOOPS, NLOOPGROUPS, NCTAMPS
      PARAMETER (NLOOPS=9, NLOOPGROUPS=9, NCTAMPS=7)
      INTEGER    NLOOPAMPS
      PARAMETER (NLOOPAMPS=16)
      INTEGER    NWAVEFUNCS,NLOOPWAVEFUNCS
      PARAMETER (NWAVEFUNCS=10,NLOOPWAVEFUNCS=21)
      REAL*16     ZERO
      PARAMETER (ZERO=0.0E0_16)
      COMPLEX*32     IZERO
      PARAMETER (IZERO=CMPLX(0.0E0_16,0.0E0_16,KIND=16))
C     These are constants related to the split orders
      INTEGER    NSO, NSQUAREDSO, NAMPSO
      PARAMETER (NSO=1, NSQUAREDSO=1, NAMPSO=2)
C     
C     ARGUMENTS
C     
      REAL*16 P(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL), IC(NEXTERNAL)
      INTEGER H
C     
C     LOCAL VARIABLES
C     
      INTEGER I,J,K
      COMPLEX*32 COEFS(MAXLWFSIZE,0:VERTEXMAXCOEFS-1,MAXLWFSIZE)
C     
C     GLOBAL VARIABLES
C     
      INCLUDE 'mp_coupl_same_name.inc'

      INTEGER GOODHEL(NCOMB)
      LOGICAL GOODAMP(NSQUAREDSO,NLOOPGROUPS)
      COMMON/FILTERS/GOODAMP,GOODHEL

      INTEGER SQSO_TARGET
      COMMON/SOCHOICE/SQSO_TARGET

      LOGICAL UVCT_REQ_SO_DONE,MP_UVCT_REQ_SO_DONE,CT_REQ_SO_DONE
     $ ,MP_CT_REQ_SO_DONE,LOOP_REQ_SO_DONE,MP_LOOP_REQ_SO_DONE
     $ ,CTCALL_REQ_SO_DONE,FILTER_SO
      COMMON/SO_REQS/UVCT_REQ_SO_DONE,MP_UVCT_REQ_SO_DONE
     $ ,CT_REQ_SO_DONE,MP_CT_REQ_SO_DONE,LOOP_REQ_SO_DONE
     $ ,MP_LOOP_REQ_SO_DONE,CTCALL_REQ_SO_DONE,FILTER_SO

      COMPLEX*32 AMP(NBORNAMPS)
      COMMON/MP_AMPS/AMP
      COMPLEX*32 W(20,NWAVEFUNCS)
      COMMON/MP_W/W

      COMPLEX*32 WL(MAXLWFSIZE,0:LOOPMAXCOEFS-1,MAXLWFSIZE,
     $ -1:NLOOPWAVEFUNCS)
      COMPLEX*32 PL(0:3,-1:NLOOPWAVEFUNCS)
      COMMON/MP_WL/WL,PL

      COMPLEX*32 AMPL(3,NCTAMPS)
      COMMON/MP_AMPL/AMPL

C     
C     ----------
C     BEGIN CODE
C     ----------

C     The target squared split order contribution is already reached
C      if true.
      IF (FILTER_SO.AND.MP_LOOP_REQ_SO_DONE) THEN
        GOTO 1001
      ENDIF

C     Coefficient construction for loop diagram with ID 4
      CALL MP_FFV1L1P0_3(PL(0,0),W(1,6),GC_5,ZERO,ZERO,PL(0,1),COEFS)
      CALL MP_UPDATE_WL_0_0(WL(1,0,1,0),4,COEFS,4,4,WL(1,0,1,1))
      CALL MP_FFV1L3_2(PL(0,1),W(1,8),GC_5,ZERO,ZERO,PL(0,2),COEFS)
      CALL MP_UPDATE_WL_0_1(WL(1,0,1,1),4,COEFS,4,4,WL(1,0,1,2))
      CALL MP_CREATE_LOOP_COEFS(WL(1,0,1,2),1,4,1,1,1,8,H)
C     Coefficient construction for loop diagram with ID 5
      CALL MP_FFV1L3_2(PL(0,0),W(1,2),GC_5,ZERO,ZERO,PL(0,3),COEFS)
      CALL MP_UPDATE_WL_0_1(WL(1,0,1,0),4,COEFS,4,4,WL(1,0,1,3))
      CALL MP_FFV1L1_2(PL(0,3),W(1,4),GC_1,ZERO,ZERO,PL(0,4),COEFS)
      CALL MP_UPDATE_WL_1_1(WL(1,0,1,3),4,COEFS,4,4,WL(1,0,1,4))
      CALL MP_FFV1L1P0_3(PL(0,4),W(1,6),GC_5,ZERO,ZERO,PL(0,5),COEFS)
      CALL MP_UPDATE_WL_2_0(WL(1,0,1,4),4,COEFS,4,4,WL(1,0,1,5))
      CALL MP_CREATE_LOOP_COEFS(WL(1,0,1,5),2,4,2,1,1,9,H)
C     Coefficient construction for loop diagram with ID 6
      CALL MP_FFV2L1_2(PL(0,3),W(1,3),GC_11,ZERO,ZERO,PL(0,6),COEFS)
      CALL MP_UPDATE_WL_1_1(WL(1,0,1,3),4,COEFS,4,4,WL(1,0,1,6))
      CALL MP_FFV1L1P0_3(PL(0,6),W(1,7),GC_5,ZERO,ZERO,PL(0,7),COEFS)
      CALL MP_UPDATE_WL_2_0(WL(1,0,1,6),4,COEFS,4,4,WL(1,0,1,7))
      CALL MP_CREATE_LOOP_COEFS(WL(1,0,1,7),2,4,3,1,1,10,H)
C     Coefficient construction for loop diagram with ID 7
      CALL MP_FFV1L3_1(PL(0,0),W(1,1),GC_5,ZERO,ZERO,PL(0,8),COEFS)
      CALL MP_UPDATE_WL_0_1(WL(1,0,1,0),4,COEFS,4,4,WL(1,0,1,8))
      CALL MP_FFV2L2_1(PL(0,8),W(1,3),GC_11,ZERO,ZERO,PL(0,9),COEFS)
      CALL MP_UPDATE_WL_1_1(WL(1,0,1,8),4,COEFS,4,4,WL(1,0,1,9))
      CALL MP_FFV1L2P0_3(PL(0,9),W(1,8),GC_5,ZERO,ZERO,PL(0,10),COEFS)
      CALL MP_UPDATE_WL_2_0(WL(1,0,1,9),4,COEFS,4,4,WL(1,0,1,10))
      CALL MP_CREATE_LOOP_COEFS(WL(1,0,1,10),2,4,4,1,1,11,H)
C     Coefficient construction for loop diagram with ID 8
      CALL MP_FFV1L1P0_3(PL(0,0),W(1,1),GC_5,ZERO,ZERO,PL(0,11),COEFS)
      CALL MP_UPDATE_WL_0_0(WL(1,0,1,0),4,COEFS,4,4,WL(1,0,1,11))
      CALL MP_FFV1L3_2(PL(0,11),W(1,2),GC_5,ZERO,ZERO,PL(0,12),COEFS)
      CALL MP_UPDATE_WL_0_1(WL(1,0,1,11),4,COEFS,4,4,WL(1,0,1,12))
      CALL MP_FFV2L1_2(PL(0,12),W(1,9),GC_11,ZERO,ZERO,PL(0,13),COEFS)
      CALL MP_UPDATE_WL_1_1(WL(1,0,1,12),4,COEFS,4,4,WL(1,0,1,13))
      CALL MP_CREATE_LOOP_COEFS(WL(1,0,1,13),2,4,5,1,1,12,H)
C     Coefficient construction for loop diagram with ID 9
      CALL MP_FFV2L1_2(PL(0,12),W(1,3),GC_11,ZERO,ZERO,PL(0,14),COEFS)
      CALL MP_UPDATE_WL_1_1(WL(1,0,1,12),4,COEFS,4,4,WL(1,0,1,14))
      CALL MP_FFV1L1_2(PL(0,14),W(1,4),GC_2,ZERO,ZERO,PL(0,15),COEFS)
      CALL MP_UPDATE_WL_2_1(WL(1,0,1,14),4,COEFS,4,4,WL(1,0,1,15))
      CALL MP_CREATE_LOOP_COEFS(WL(1,0,1,15),3,4,6,1,1,13,H)
C     Coefficient construction for loop diagram with ID 10
      CALL MP_FFV1L1_2(PL(0,12),W(1,4),GC_1,ZERO,ZERO,PL(0,16),COEFS)
      CALL MP_UPDATE_WL_1_1(WL(1,0,1,12),4,COEFS,4,4,WL(1,0,1,16))
      CALL MP_FFV2L1_2(PL(0,16),W(1,3),GC_11,ZERO,ZERO,PL(0,17),COEFS)
      CALL MP_UPDATE_WL_2_1(WL(1,0,1,16),4,COEFS,4,4,WL(1,0,1,17))
      CALL MP_CREATE_LOOP_COEFS(WL(1,0,1,17),3,4,7,1,1,14,H)
C     Coefficient construction for loop diagram with ID 11
      CALL MP_FFV1L2P0_3(PL(0,0),W(1,10),GC_5,ZERO,ZERO,PL(0,18),COEFS)
      CALL MP_UPDATE_WL_0_0(WL(1,0,1,0),4,COEFS,4,4,WL(1,0,1,18))
      CALL MP_FFV1L3_1(PL(0,18),W(1,7),GC_5,ZERO,ZERO,PL(0,19),COEFS)
      CALL MP_UPDATE_WL_0_1(WL(1,0,1,18),4,COEFS,4,4,WL(1,0,1,19))
      CALL MP_CREATE_LOOP_COEFS(WL(1,0,1,19),1,4,8,1,1,15,H)
C     Coefficient construction for loop diagram with ID 12
      CALL MP_FFV1L2_1(PL(0,8),W(1,4),GC_2,ZERO,ZERO,PL(0,20),COEFS)
      CALL MP_UPDATE_WL_1_1(WL(1,0,1,8),4,COEFS,4,4,WL(1,0,1,20))
      CALL MP_FFV1L2P0_3(PL(0,20),W(1,10),GC_5,ZERO,ZERO,PL(0,21)
     $ ,COEFS)
      CALL MP_UPDATE_WL_2_0(WL(1,0,1,20),4,COEFS,4,4,WL(1,0,1,21))
      CALL MP_CREATE_LOOP_COEFS(WL(1,0,1,21),2,4,9,1,1,16,H)
C     At this point, all loop coefficients needed for (QCD=2), i.e. of
C      split order ID=1, are computed.
      IF(FILTER_SO.AND.SQSO_TARGET.EQ.1) GOTO 4000

      GOTO 1001
 4000 CONTINUE
      MP_LOOP_REQ_SO_DONE=.TRUE.
 1001 CONTINUE
      END

