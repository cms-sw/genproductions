      SUBROUTINE HELAS_CALLS_UVCT_1(P,NHEL,H,IC)
C     
C     Modules
C     
      USE POLYNOMIAL_CONSTANTS
C     
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=5)
      INTEGER    NCOMB
      PARAMETER (NCOMB=48)
      INTEGER NBORNAMPS
      PARAMETER (NBORNAMPS=8)
      INTEGER    NLOOPS, NLOOPGROUPS, NCTAMPS
      PARAMETER (NLOOPS=71, NLOOPGROUPS=47, NCTAMPS=116)
      INTEGER    NLOOPAMPS
      PARAMETER (NLOOPAMPS=187)
      INTEGER    NWAVEFUNCS,NLOOPWAVEFUNCS
      PARAMETER (NWAVEFUNCS=26,NLOOPWAVEFUNCS=142)
      REAL*8     ZERO
      PARAMETER (ZERO=0D0)
      REAL*16     MP__ZERO
      PARAMETER (MP__ZERO=0.0E0_16)
C     These are constants related to the split orders
      INTEGER    NSO, NSQUAREDSO, NAMPSO
      PARAMETER (NSO=1, NSQUAREDSO=1, NAMPSO=2)
C     
C     ARGUMENTS
C     
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL), IC(NEXTERNAL)
      INTEGER H
C     
C     LOCAL VARIABLES
C     
      INTEGER I,J,K
      COMPLEX*16 COEFS(MAXLWFSIZE,0:VERTEXMAXCOEFS-1,MAXLWFSIZE)

      LOGICAL DUMMYFALSE
      DATA DUMMYFALSE/.FALSE./
C     
C     GLOBAL VARIABLES
C     
      INCLUDE 'coupl.inc'
      INCLUDE 'mp_coupl.inc'

      INTEGER HELOFFSET
      INTEGER GOODHEL(NCOMB)
      LOGICAL GOODAMP(NSQUAREDSO,NLOOPGROUPS)
      COMMON/FILTERS/GOODAMP,GOODHEL,HELOFFSET

      LOGICAL CHECKPHASE
      LOGICAL HELDOUBLECHECKED
      COMMON/INIT/CHECKPHASE, HELDOUBLECHECKED

      INTEGER SQSO_TARGET
      COMMON/SOCHOICE/SQSO_TARGET

      LOGICAL UVCT_REQ_SO_DONE,MP_UVCT_REQ_SO_DONE,CT_REQ_SO_DONE
     $ ,MP_CT_REQ_SO_DONE,LOOP_REQ_SO_DONE,MP_LOOP_REQ_SO_DONE
     $ ,CTCALL_REQ_SO_DONE,FILTER_SO
      COMMON/SO_REQS/UVCT_REQ_SO_DONE,MP_UVCT_REQ_SO_DONE
     $ ,CT_REQ_SO_DONE,MP_CT_REQ_SO_DONE,LOOP_REQ_SO_DONE
     $ ,MP_LOOP_REQ_SO_DONE,CTCALL_REQ_SO_DONE,FILTER_SO

      INTEGER I_SO
      COMMON/I_SO/I_SO
      INTEGER I_LIB
      COMMON/I_LIB/I_LIB

      COMPLEX*16 AMP(NBORNAMPS)
      COMMON/AMPS/AMP
      COMPLEX*16 W(20,NWAVEFUNCS)
      COMMON/W/W

      COMPLEX*16 WL(MAXLWFSIZE,0:LOOPMAXCOEFS-1,MAXLWFSIZE,
     $ -1:NLOOPWAVEFUNCS)
      COMPLEX*16 PL(0:3,-1:NLOOPWAVEFUNCS)
      COMMON/WL/WL,PL

      COMPLEX*16 AMPL(3,NCTAMPS)
      COMMON/AMPL/AMPL

C     
C     ----------
C     BEGIN CODE
C     ----------

C     The target squared split order contribution is already reached
C      if true.
      IF (FILTER_SO.AND.UVCT_REQ_SO_DONE) THEN
        GOTO 1001
      ENDIF

C     Amplitude(s) for UVCT diagram with ID 80
      CALL FFV2_0(W(1,5),W(1,6),W(1,7),GC_11,AMPL(2,101))
      AMPL(2,101)=AMPL(2,101)*(1.0D0*UVWFCT_G_2_1EPS)
C     Amplitude(s) for UVCT diagram with ID 81
      CALL FFV2_0(W(1,5),W(1,6),W(1,7),GC_11,AMPL(1,102))
      AMPL(1,102)=AMPL(1,102)*(1.0D0*UVWFCT_G_2)
C     Amplitude(s) for UVCT diagram with ID 82
      CALL FFV1_0(W(1,8),W(1,6),W(1,4),GC_1,AMPL(2,103))
      AMPL(2,103)=AMPL(2,103)*(1.0D0*UVWFCT_G_2_1EPS)
C     Amplitude(s) for UVCT diagram with ID 83
      CALL FFV1_0(W(1,8),W(1,6),W(1,4),GC_1,AMPL(1,104))
      AMPL(1,104)=AMPL(1,104)*(1.0D0*UVWFCT_G_2)
C     Amplitude(s) for UVCT diagram with ID 84
      CALL FFV2_0(W(1,9),W(1,6),W(1,3),GC_11,AMPL(2,105))
      AMPL(2,105)=AMPL(2,105)*(1.0D0*UVWFCT_G_2_1EPS)
C     Amplitude(s) for UVCT diagram with ID 85
      CALL FFV2_0(W(1,9),W(1,6),W(1,3),GC_11,AMPL(1,106))
      AMPL(1,106)=AMPL(1,106)*(1.0D0*UVWFCT_G_2)
C     Amplitude(s) for UVCT diagram with ID 86
      CALL FFV1_0(W(1,11),W(1,10),W(1,4),GC_2,AMPL(2,107))
      AMPL(2,107)=AMPL(2,107)*(1.0D0*UVWFCT_G_2_1EPS)
C     Amplitude(s) for UVCT diagram with ID 87
      CALL FFV1_0(W(1,11),W(1,10),W(1,4),GC_2,AMPL(1,108))
      AMPL(1,108)=AMPL(1,108)*(1.0D0*UVWFCT_G_2)
C     Amplitude(s) for UVCT diagram with ID 88
      CALL FFV1_0(W(1,9),W(1,10),W(1,2),GC_5,AMPL(2,109))
      AMPL(2,109)=AMPL(2,109)*(1.0D0*UVWFCT_G_2_1EPS)
C     Amplitude(s) for UVCT diagram with ID 89
      CALL FFV1_0(W(1,9),W(1,10),W(1,2),GC_5,AMPL(1,110))
      AMPL(1,110)=AMPL(1,110)*(1.0D0*UVWFCT_G_2)
C     Amplitude(s) for UVCT diagram with ID 90
      CALL FFV2_0(W(1,11),W(1,12),W(1,3),GC_11,AMPL(2,111))
      AMPL(2,111)=AMPL(2,111)*(1.0D0*UVWFCT_G_2_1EPS)
C     Amplitude(s) for UVCT diagram with ID 91
      CALL FFV2_0(W(1,11),W(1,12),W(1,3),GC_11,AMPL(1,112))
      AMPL(1,112)=AMPL(1,112)*(1.0D0*UVWFCT_G_2)
C     Amplitude(s) for UVCT diagram with ID 92
      CALL FFV1_0(W(1,8),W(1,12),W(1,2),GC_5,AMPL(2,113))
      AMPL(2,113)=AMPL(2,113)*(1.0D0*UVWFCT_G_2_1EPS)
C     Amplitude(s) for UVCT diagram with ID 93
      CALL FFV1_0(W(1,8),W(1,12),W(1,2),GC_5,AMPL(1,114))
      AMPL(1,114)=AMPL(1,114)*(1.0D0*UVWFCT_G_2)
C     Amplitude(s) for UVCT diagram with ID 94
      CALL FFV2_0(W(1,11),W(1,1),W(1,7),GC_11,AMPL(2,115))
      AMPL(2,115)=AMPL(2,115)*(1.0D0*UVWFCT_G_2_1EPS)
C     Amplitude(s) for UVCT diagram with ID 95
      CALL FFV2_0(W(1,11),W(1,1),W(1,7),GC_11,AMPL(1,116))
      AMPL(1,116)=AMPL(1,116)*(1.0D0*UVWFCT_G_2)
C     At this point, all UVCT amps needed for (QCD=4), i.e. of split
C      order ID=1, are computed.
      IF(FILTER_SO.AND.SQSO_TARGET.EQ.1) GOTO 3000

      GOTO 1001
 3000 CONTINUE
      UVCT_REQ_SO_DONE=.TRUE.
 1001 CONTINUE
      END

