        IF(IPROC.EQ.256) DSIGPROC=DSIG256(P1,WGT,IMODE)  ! d~ b > e- ve~ c b~
        IF(IPROC.EQ.257) DSIGPROC=DSIG257(P1,WGT,IMODE)  ! s~ b > e- ve~ u d~
        IF(IPROC.EQ.258) DSIGPROC=DSIG258(P1,WGT,IMODE)  ! s~ b > e- ve~ u b~
        IF(IPROC.EQ.259) DSIGPROC=DSIG259(P1,WGT,IMODE)  ! s~ b > e- ve~ c d~
        IF(IPROC.EQ.260) DSIGPROC=DSIG260(P1,WGT,IMODE)  ! s~ b > e- ve~ c b~
        IF(IPROC.EQ.261) DSIGPROC=DSIG261(P1,WGT,IMODE)  ! b b > e- ve~ u d
        IF(IPROC.EQ.262) DSIGPROC=DSIG262(P1,WGT,IMODE)  ! b b > e- ve~ u s
        IF(IPROC.EQ.263) DSIGPROC=DSIG263(P1,WGT,IMODE)  ! b b > e- ve~ c d
        IF(IPROC.EQ.264) DSIGPROC=DSIG264(P1,WGT,IMODE)  ! b b > e- ve~ c s
      ENDIF

      IF (LAST_ICONF.NE.-1.AND.IMIRROR.EQ.2) THEN
C       Flip back local momenta P1 if cached
        DO I=1,NEXTERNAL
          P1(2,I)=-P1(2,I)
          P1(3,I)=-P1(3,I)
        ENDDO
      ENDIF

      RETURN

      END

C     -----------------------------------------
C     Subroutine to map three positive integers
C     I, J and K with upper bounds J_bound and
C     K_bound to a one_dimensional
C     index L
C     -----------------------------------------

      SUBROUTINE MAP_3_TO_1(I,J,K,J_BOUND,K_BOUND,L)
      IMPLICIT NONE
      INTEGER, INTENT(IN)  :: I,J,K,J_BOUND,K_BOUND
      INTEGER, INTENT(OUT) :: L

      L = I*(J_BOUND*(K_BOUND+1)+K_BOUND+1)+J*(K_BOUND+1)+K

      END SUBROUTINE MAP_3_TO_1

C     -----------------------------------------
C     Subroutine to map back the positive 
C     integer L to the three integers 
C     I, J and K with upper bounds
C     J_bound and K_bound.
C     -----------------------------------------

      SUBROUTINE MAP_1_TO_3(L,J_BOUND,K_BOUND,I,J,K)
      IMPLICIT NONE
      INTEGER, INTENT(OUT)  :: I,J,K
      INTEGER, INTENT(IN)   :: L, J_BOUND, K_BOUND
      INTEGER               :: L_RUN

      L_RUN = L
      I = L_RUN/(J_BOUND*(K_BOUND+1)+K_BOUND+1)
      L_RUN = L_RUN - I*((J_BOUND*(K_BOUND+1)+K_BOUND+1))
      J = L_RUN/(K_BOUND+1)
      L_RUN = L_RUN - J*(K_BOUND+1)
      K  = L_RUN

      END SUBROUTINE MAP_1_TO_3


